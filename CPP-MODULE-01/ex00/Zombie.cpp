#include "Zombie.hpp"

Zombie::Zombie(void)
{
}

Zombie::Zombie(std::string _s)
{
	this->_name = _s;
}

Zombie::~Zombie()
{
	std::cout << "Zombie called " << this->_name << " was destroyed" << std::endl;
}

void	Zombie::announce(void)
{
	std::cout << this->_name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}